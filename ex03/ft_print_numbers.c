#include <unistd.h>

void	ft_putchar(char c);

void	ft_print_numbers(void)
{
	char	c;

	c = '0';
	while (c <= '9')
	{
		ft_putchar(c++);
	}
	return ;
}

void	ft_putchar(char c)
{
	write (1, &c, 1);
	return ;
}
